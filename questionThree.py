# 3 - Realiza a leitura de 1 int e imprime o dobro, triplo e a metade do valor lido.
number = int(input('Digite um número: '))
#Calculations
double = number * 2
triple = number * 3
half = number / 2
print('O dobro do valor digitado é: {}\nO triplo do valor digitado é: {}\nA metade do valor digitado é: {}'.format(double, triple, half))